export function getRandomProducts(noOfProducts, productType, data = _data) {
 let temp = data[productType];
 let random = (Math.floor(Math.random() * temp.length));
 let products = [];
 for (let i = 0; i < noOfProducts; i++) {
  if (random + i >= temp.length) random = 0;
  products.push(temp[random + i]);
 }
 temp = data[productType];
 return products;
}

